# Modules and pip in python!
 
'''Module is like a code library which can be used to borrow the code written by somebody else in our python program.

There are two  types of modules in python:

1. Built-in modules: These are the modules that come with Python installation, like `math`, `os`, etc.
2. External modules: These modules are imported from a third-party file or can be installed using a package manager like pip or conda. Since this 
code is written by somebody else, we can use it in our code by importing it.'''

# The pip command.
'''The pip command is used to install the external modules in python. It is basicall a package manager which finds the package available publicly and installs it in our system.
To install a package, we can use the command `pip install package_name`.'''



