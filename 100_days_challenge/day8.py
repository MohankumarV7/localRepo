#type casting

''' Explicit Type Casting
In Python, we can convert one data type to another using functions like int(), float(), str(), etc. This is known as explicit type casting.

Implicit Type Casting
Python automatically converts one data type to another when it is necessary. This is known as implicit type casting.
For example, if we add an integer and a float, Python will automatically convert the integer to a float before performing the addition.    '''

# Example of explicit type casting
x = 5
y = float(x)    # x is converted to float